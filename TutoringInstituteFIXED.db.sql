BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS Attendance (
    Attendance_ID TEXT PRIMARY KEY,
    Enrollment_ID TEXT,
    AttendanceDate TEXT,
    Attendance_Status TEXT,
    FOREIGN KEY (Enrollment_ID) REFERENCES Enrollment(Enrollment_ID)
);
CREATE TABLE IF NOT EXISTS Batch (
    Batch_ID TEXT PRIMARY KEY,
    Batch_Name TEXT,
    Subject_ID TEXT,
    Teacher_ID TEXT,
    Start_Date TEXT,
    End_date TEXT,
    Schedule TEXT,
    Fee_amount REAL,
    RoomNumber TEXT,
    FOREIGN KEY (Subject_ID) REFERENCES Subject(Subject_ID),
    FOREIGN KEY (Teacher_ID) REFERENCES Teacher(Teacher_ID)
);
CREATE TABLE IF NOT EXISTS Enrollment (
    Enrollment_ID    VARCHAR2(30) NOT NULL,
    Student_ID       VARCHAR2(10) NOT NULL,
    Batch_ID         VARCHAR2(30) NOT NULL,
    EnrollmentDate   DATE,
    EnrollmentStatus VARCHAR2(20),
    CONSTRAINT Enrollment_PK PRIMARY KEY (Enrollment_ID),
    CONSTRAINT Enrollment_Student_FK FOREIGN KEY (Student_ID)
        REFERENCES Student(Student_id),
    CONSTRAINT Enrollment_Batch_FK FOREIGN KEY (Batch_ID)
        REFERENCES Batch(Batch_ID)
);
CREATE TABLE IF NOT EXISTS FeePayment (
    Payment_ID TEXT PRIMARY KEY,
    Student_ID TEXT,
    Batch_ID TEXT,
    PaymentDate TEXT,
    AmountPaid REAL,
    PaymentMethod TEXT,
    Payment_Status TEXT,
    FOREIGN KEY (Student_ID) REFERENCES Student(Student_ID),
    FOREIGN KEY (Batch_ID) REFERENCES Batch(Batch_ID)
);
CREATE TABLE IF NOT EXISTS Result (
    Result_ID TEXT PRIMARY KEY,
    Test_ID TEXT,
    Student_ID TEXT,
    Marks_Obtained REAL,
    Grade TEXT,
    FOREIGN KEY (Test_ID) REFERENCES Test(Test_ID),
    FOREIGN KEY (Student_ID) REFERENCES Student(Student_ID)
);
CREATE TABLE IF NOT EXISTS Student (
    Student_ID TEXT PRIMARY KEY,
    Full_Name TEXT,
    Gender TEXT,
    Date_Of_Birth TEXT,
    Phone_Number TEXT,
    Email TEXT,
    Guardian_Name TEXT,
    Guardian_Phone TEXT,
    Address TEXT,
    JoinDate TEXT
);
CREATE TABLE IF NOT EXISTS Subject (
    Subject_ID TEXT PRIMARY KEY,
    Subject_Name TEXT,
    SubjectLevel TEXT
);
CREATE TABLE IF NOT EXISTS Teacher (
    Teacher_ID TEXT PRIMARY KEY,
    Full_Name TEXT,
    Phone_Number TEXT,
    Email TEXT,
    Qualification TEXT,
    Hire_Date TEXT,
    Subject TEXT,
    Subject_ID TEXT,
    Subject_Name TEXT,
    Subject_Level TEXT
);
CREATE TABLE IF NOT EXISTS Test (
    Test_ID TEXT PRIMARY KEY,
    Batch_ID TEXT,
    TestName TEXT,
    TestDate TEXT,
    TotalMarks REAL,
    FOREIGN KEY (Batch_ID) REFERENCES Batch(Batch_ID)
);
